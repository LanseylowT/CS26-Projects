    public void switchScreen(String panelName){
        CustomFadingPanel fadingPanel = new CustomFadingPanel();
        mainBody.add(fadingPanel, "FadingPanel");
        cardLayout.show(mainBody, "FadingPanel");

        Timer timer = new Timer(200, (e) -> {
           fadingPanel.startFadeOut();
           cardLayout.show(mainBody, panelName);
        });
        timer.setRepeats(false);
        timer.start();
    }

